GRANT APPLY ON ROW ACCESS POLICY
PHARMARETAIL_AI_CONTROL_TOWER.GOVERNANCE.OPERATIONAL_STORE_REGION_POLICY
TO ROLE PHARMARETAIL_DBT;
GRANT APPLY ON MASKING POLICY
PHARMARETAIL_AI_CONTROL_TOWER.GOVERNANCE.SENSITIVE_TEXT_MASK
TO ROLE PHARMARETAIL_DBT;

GRANT SELECT ON TABLE PHARMARETAIL_AI_CONTROL_TOWER.GOVERNANCE.USER_STORE_SCOPE
TO ROLE PHARMARETAIL_AI_APP;
GRANT SELECT ON TABLE PHARMARETAIL_AI_CONTROL_TOWER.GOVERNANCE.USER_REGION_SCOPE
TO ROLE PHARMARETAIL_AI_APP;
GRANT INSERT ON TABLE PHARMARETAIL_AI_CONTROL_TOWER.AI_LOGS.OPERATIONAL_ACCESS_AUDIT
TO ROLE PHARMARETAIL_AI_APP;

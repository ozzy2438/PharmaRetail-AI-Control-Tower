    QUERY_HASH VARCHAR NOT NULL,
    COUNTRY_FILTER VARCHAR,
    BUSINESS_UNIT_FILTER VARCHAR,
    AS_OF_DATE DATE NOT NULL,
    RETRIEVED_CHUNK_IDS VARIANT NOT NULL,
    CITATION_COUNT NUMBER NOT NULL,
    UNCERTAINTY VARCHAR NOT NULL,
    REFUSED BOOLEAN NOT NULL,
    REFUSAL_REASON VARCHAR,
    OUTCOME VARCHAR NOT NULL,
    LATENCY_MS NUMBER NOT NULL,
    CONSTRAINT PK_RETRIEVAL_AUDIT PRIMARY KEY (AUDIT_ID)
);

CREATE TABLE IF NOT EXISTS PHARMARETAIL_AI_CONTROL_TOWER.GOVERNANCE.RAG_ROLE_ACCESS_SCOPE (
    ROLE_NAME VARCHAR NOT NULL,
    ACCESS_LEVEL VARCHAR NOT NULL,
    UPDATED_AT TIMESTAMP_NTZ NOT NULL,
    CONSTRAINT PK_RAG_ROLE_ACCESS_SCOPE PRIMARY KEY (ROLE_NAME, ACCESS_LEVEL),
    CONSTRAINT CK_RAG_SCOPE_ACCESS CHECK (ACCESS_LEVEL IN ('PUBLIC', 'INTERNAL', 'RESTRICTED'))
);

MERGE INTO PHARMARETAIL_AI_CONTROL_TOWER.GOVERNANCE.RAG_ROLE_ACCESS_SCOPE AS target
USING (
    SELECT COLUMN1 AS ROLE_NAME, COLUMN2 AS ACCESS_LEVEL, CURRENT_TIMESTAMP() AS UPDATED_AT
    FROM VALUES
        ('PHARMARETAIL_ADMIN', 'PUBLIC'),
        ('PHARMARETAIL_ADMIN', 'INTERNAL'),
        ('PHARMARETAIL_ADMIN', 'RESTRICTED'),
        ('PHARMARETAIL_AI_APP', 'PUBLIC'),
        ('PHARMARETAIL_AI_APP', 'INTERNAL'),
        ('PHARMARETAIL_AI_APP', 'RESTRICTED'),
        ('PHARMARETAIL_SUPPLY_CHAIN_ANALYST', 'PUBLIC'),
        ('PHARMARETAIL_SUPPLY_CHAIN_ANALYST', 'INTERNAL'),
        ('PHARMARETAIL_SUPPLY_CHAIN_ANALYST', 'RESTRICTED'),
        ('PHARMARETAIL_AREA_MANAGER', 'PUBLIC'),
        ('PHARMARETAIL_AREA_MANAGER', 'INTERNAL'),
        ('PHARMARETAIL_STORE_MANAGER', 'PUBLIC'),
        ('PHARMARETAIL_STORE_MANAGER', 'INTERNAL'),
        ('PHARMARETAIL_READONLY', 'PUBLIC')
) AS source
ON target.ROLE_NAME = source.ROLE_NAME AND target.ACCESS_LEVEL = source.ACCESS_LEVEL
WHEN MATCHED THEN UPDATE SET UPDATED_AT = source.UPDATED_AT
WHEN NOT MATCHED THEN INSERT (ROLE_NAME, ACCESS_LEVEL, UPDATED_AT)
VALUES (source.ROLE_NAME, source.ACCESS_LEVEL, source.UPDATED_AT);


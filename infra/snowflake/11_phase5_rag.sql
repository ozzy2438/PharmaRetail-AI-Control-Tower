USE ROLE PHARMARETAIL_ADMIN;
ALTER SESSION SET QUERY_TAG = 'PHARMARETAIL_PHASE5_RAG_DDL';

CREATE TABLE IF NOT EXISTS PHARMARETAIL_AI_CONTROL_TOWER.GOVERNANCE.DOCUMENT_REGISTRY (
    DOC_ID VARCHAR NOT NULL,
    TITLE VARCHAR NOT NULL,
    VERSION VARCHAR NOT NULL,
    EFFECTIVE_DATE DATE NOT NULL,
    EXPIRY_DATE DATE NOT NULL,
    COUNTRY VARCHAR NOT NULL,
    BUSINESS_UNIT VARCHAR NOT NULL,
    POLICY_OWNER VARCHAR NOT NULL,
    ACCESS_LEVEL VARCHAR NOT NULL,
    SECTION_ID VARCHAR NOT NULL,
    SOURCE_TYPE VARCHAR NOT NULL,
    SOURCE_FILE VARCHAR NOT NULL,
    CONTENT_HASH VARCHAR NOT NULL,
    DOCUMENT_STATUS VARCHAR NOT NULL,
    INGESTED_AT TIMESTAMP_NTZ NOT NULL,
    CONSTRAINT PK_DOCUMENT_REGISTRY PRIMARY KEY (DOC_ID, VERSION),
    CONSTRAINT CK_DOCUMENT_DATES CHECK (EFFECTIVE_DATE <= EXPIRY_DATE),
    CONSTRAINT CK_DOCUMENT_ACCESS CHECK (ACCESS_LEVEL IN ('PUBLIC', 'INTERNAL', 'RESTRICTED'))
);

CREATE TABLE IF NOT EXISTS PHARMARETAIL_AI_CONTROL_TOWER.GOVERNANCE.DOCUMENT_CHUNKS (
    CHUNK_ID VARCHAR NOT NULL,
    DOC_ID VARCHAR NOT NULL,
    VERSION VARCHAR NOT NULL,
    SECTION_ID VARCHAR NOT NULL,
    SECTION_TITLE VARCHAR NOT NULL,
    CHUNK_INDEX NUMBER NOT NULL,
    CHUNK_TEXT VARCHAR NOT NULL,
    TOKEN_COUNT NUMBER NOT NULL,
    CONTENT_HASH VARCHAR NOT NULL,
    COUNTRY VARCHAR NOT NULL,
    BUSINESS_UNIT VARCHAR NOT NULL,
    ACCESS_LEVEL VARCHAR NOT NULL,
    EFFECTIVE_DATE DATE NOT NULL,
    EXPIRY_DATE DATE NOT NULL,
    EMBEDDING VARIANT NOT NULL,
    CREATED_AT TIMESTAMP_NTZ NOT NULL,
    CONSTRAINT PK_DOCUMENT_CHUNKS PRIMARY KEY (CHUNK_ID),
    CONSTRAINT FK_CHUNK_DOCUMENT FOREIGN KEY (DOC_ID, VERSION)
        REFERENCES PHARMARETAIL_AI_CONTROL_TOWER.GOVERNANCE.DOCUMENT_REGISTRY (DOC_ID, VERSION),
    CONSTRAINT CK_CHUNK_DATES CHECK (EFFECTIVE_DATE <= EXPIRY_DATE),
    CONSTRAINT CK_CHUNK_ACCESS CHECK (ACCESS_LEVEL IN ('PUBLIC', 'INTERNAL', 'RESTRICTED'))
);

CREATE TABLE IF NOT EXISTS PHARMARETAIL_AI_CONTROL_TOWER.GOVERNANCE.EMBEDDING_METADATA (
    CHUNK_ID VARCHAR NOT NULL,
    EMBEDDING_MODEL VARCHAR NOT NULL,
    EMBEDDING_VERSION VARCHAR NOT NULL,
    EMBEDDING_DIMENSION NUMBER NOT NULL,
    EMBEDDING_PROVIDER VARCHAR NOT NULL,
    DISTANCE_METRIC VARCHAR NOT NULL,
    CONTENT_HASH VARCHAR NOT NULL,
    GENERATED_AT TIMESTAMP_NTZ NOT NULL,
    CONSTRAINT PK_EMBEDDING_METADATA PRIMARY KEY (CHUNK_ID),
    CONSTRAINT FK_EMBEDDING_CHUNK FOREIGN KEY (CHUNK_ID)
        REFERENCES PHARMARETAIL_AI_CONTROL_TOWER.GOVERNANCE.DOCUMENT_CHUNKS (CHUNK_ID)
);

CREATE TABLE IF NOT EXISTS PHARMARETAIL_AI_CONTROL_TOWER.GOVERNANCE.RETRIEVAL_AUDIT (
